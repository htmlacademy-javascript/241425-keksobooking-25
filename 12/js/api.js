
function loadMarkersData(onSuccess, onError) {
  fetch('https://25.javascript.pages.academy/keksobooking/data')
    .then((response) => {
      if (response.ok) {
        return response.json();
      }

      throw new Error(`${response.status} ${response.statusText}`);
    })
    .then((data) => {
      onSuccess(data);
    })
    .catch((err) => {
      onError(err);
    });
}


function sendAdvertData(onSuccess, onError, body) {
  return fetch('https://25.javascript.pages.academy/keksobooking',
    {
      method: 'POST',
      body,
    },
  )
    .then((response) => {
      if (response.ok) {
        onSuccess();
      } else {
        onError('Не удалось отправить');
      }
    })
    .catch((err) => {
      onError(err);
    });
}

export { loadMarkersData, sendAdvertData };
